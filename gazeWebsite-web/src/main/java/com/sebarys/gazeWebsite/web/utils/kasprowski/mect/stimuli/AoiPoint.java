package com.sebarys.gazeWebsite.web.utils.kasprowski.mect.stimuli;

public class AoiPoint {
	double x;
	double y;
	
	public AoiPoint() {
		
	}
	public AoiPoint(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
